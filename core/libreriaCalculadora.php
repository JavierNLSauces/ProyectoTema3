<?php
    /**
     * Funcion que suma dos numeros introducidos como parametro
     * @param float|int $numero1
     * @param float|int $numero2
     * @return float|int la suma los dos numeros introducidos
     */
    function suma($numero1,$numero2){
        return $numero1+$numero2;
    }
    
    /**
     * Funcion que resta dos numeros introducidos como parametro
     * @param float|int $numero1
     * @param float|int $numero2
     * @return float|int la resta los dos numeros introducidos
     */
    function resta($numero1,$numero2){
        return $numero1-$numero2;
    }
    
    /**
     * Funcion que multiplica dos numeros introducidos como parametro
     * @param float|int $numero1
     * @param float|int $numero2
     * @return float|int la multiplicacion los dos numeros introducidos
     */
    function multiplicacion($numero1,$numero2){
        return $numero1*$numero2;
    }
    
    /**
     * Funcion que divide dos numeros introducidos como parametro
     * @param float|int $numero1
     * @param float|int $numero2
     * @return float|int la division de los dos numeros introducidos
     */
    function division($numero1,$numero2){
        return $numero1/$numero2;
    }
?>



